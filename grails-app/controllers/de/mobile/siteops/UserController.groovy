package de.mobile.siteops

import grails.plugins.springsecurity.Secured

@Secured(['ROLE_ADMIN'])
class UserController {

    def springSecurityService

    static allowedMethods = [save: "POST", update: "POST", delete: "POST"]

    def index = {
        redirect(action: "list", params: params)
    }

    def list = {
        params.max = Math.min(params.max ? params.int('max') : 200, 200)
        [userInstanceList: SecUser.list(params), userInstanceTotal: SecUser.count()]
    }

    def create = {
        def userInstance = new SecUser()
        userInstance.properties = params
        return [userInstance: userInstance]
    }

    def save = {
        def userInstance = new SecUser(params)

        // validate password
        def password = params.password?.trim()
        def passwordConfirmation = params.passwordConfirmation?.trim()

        if (password && password != passwordConfirmation) {
            userInstance.errors.rejectValue('password', 'user.password.match',
                ['password', 'User', password] as Object[],
                'Property [{0}] of class [{1}] does not match the confirmation')
        }

        if (!userInstance.hasErrors() && userInstance.save(flush: true)) {
            flash.message = "${message(code: 'default.created.message', args: [message(code: 'user.label', default: 'User'), userInstance.id])}"

            // handle roles
            def roles = processMultipleSelect('roles').collect { SecRole.get(it) }
            roles.each { SecRole role ->
                SecUserSecRole.create(userInstance, role, false)
            }

            redirect(action: "show", id: userInstance.id)
        }
        else {
            render(view: "create", model: [userInstance: userInstance])
        }
    }

    def show = {
        def userInstance = SecUser.get(params.id)
        if (!userInstance) {
            flash.message = "${message(code: 'default.not.found.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
            redirect(action: "list")
        }
        else {
            [userInstance: userInstance]
        }
    }

    def edit = {
        def userInstance = SecUser.get(params.id)
        if (!userInstance) {
            flash.message = "${message(code: 'default.not.found.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
            redirect(action: "list")
        }
        else {
            return [userInstance: userInstance]
        }
    }

    def update = {
        def userInstance = SecUser.get(params.id)
        if (userInstance) {
            if (params.version) {
                def version = params.version.toLong()
                if (userInstance.version > version) {
                    
                    userInstance.errors.rejectValue("version", "default.optimistic.locking.failure", [message(code: 'user.label', default: 'User')] as Object[], "Another user has updated this User while you were editing")
                    render(view: "edit", model: [userInstance: userInstance])
                    return
                }
            }

            // handle updates to roles
            def roles = processMultipleSelect('roles').collect { SecRole.get(it) }
            def currentRoles =  userInstance.getAuthorities()
            def rolesForDeletion =  (currentRoles - roles)
            def rolesForAddition = (roles - currentRoles)

            rolesForDeletion.each { SecRole role ->
                SecUserSecRole.remove(userInstance, role, true)
            }
            rolesForAddition.each { SecRole role ->
                SecUserSecRole.create(userInstance, role, true)
            }

            userInstance.properties = params

            // validate password
            def password = params.password?.trim()
            def passwordConfirmation = params.passwordConfirmation?.trim()

            userInstance.password = password
            userInstance.passwordConfirmation = passwordConfirmation

            if (password && password != passwordConfirmation) {
               userInstance.errors.rejectValue('password', 'user.password.match',
                ['password', 'User', password] as Object[],
                'Property [{0}] of class [{1}] does not match the confirmation')
            }

            if (!userInstance.hasErrors() && userInstance.save(flush: true)) {
                flash.message = "${message(code: 'default.updated.message', args: [message(code: 'user.label', default: 'User'), userInstance.id])}"
                redirect(action: "show", id: userInstance.id)
            }
            else {
                render(view: "edit", model: [userInstance: userInstance])
            }
        }
        else {
            flash.message = "${message(code: 'default.not.found.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
            redirect(action: "list")
        }
    }

    def delete = {
        def userInstance = SecUser.get(params.id)
        if (userInstance) {
            try {
                // remove all roles for this use first
                SecUserSecRole.removeAll(userInstance)

                userInstance.delete(flush: true)
                flash.message = "${message(code: 'default.deleted.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
                redirect(action: "list")
            }
            catch (org.springframework.dao.DataIntegrityViolationException e) {
                flash.message = "${message(code: 'default.not.deleted.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
                redirect(action: "show", id: params.id)
            }
        }
        else {
            flash.message = "${message(code: 'default.not.found.message', args: [message(code: 'user.label', default: 'User'), params.id])}"
            redirect(action: "list")
        }
    }


    def processMultipleSelect(String value) {
        return params.list(value)
    }
}
